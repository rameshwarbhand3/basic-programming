let fruits=["apple","mango","grapes"];
console.log(fruits);
//push
fruits.push("banana");
console.log(fruits);
//pop
let poppedfruit=fruits.pop();
console.log("popped fruit is",poppedfruit);
console.log(fruits);
//unshift;
fruits.unshift("banana");
console.log(fruits);
//shift
let removedfruit=fruits.shift();
console.log("removed fruit is",removedfruit);
console.log(fruits);